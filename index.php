            <h2>The Task</h2>
            <p><strong>"Making someone's day easier."</strong></p>
            <p>The purpose of this hack day is to have fun and explore out of the box thinking. The theme should provide you with guidance. As long as you can justify your projects relationship with the theme you are free to do whatever you want.</p>
            <p>Your work will be judged on originality, vision, ‘out of the box thinking’, creativity and business value. </p>
            <h2>Demo</h2>
            <p>Demonstrations will take place on the <strong>19th June 2013</strong> once everyone has completed. Every team will participate in demos, regardless of the quality or completeness. Each project will have a url and demos will take place in a showroom on a projector with a fixed time limit of 5 to 10 mins</p>
            <p>Anyone and everyone is invited to come have a look at what we produce, hopefully you can join us.</p>
