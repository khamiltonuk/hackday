            <h2>About</h2>
            <p><strong>The hack day is aimed to be an intense piece of research and development resulting in software built over the course of one day.</strong></p>
            <p>This day will not fill pitfalls with our sites but instead should be viewed as a research task, and a chance to learn new skills.</p>
            <p>Expectations are for teams to deliver a working demo of their idea. </p>
            <p>It will be in your best interest not to talk to your colleagues about your projects to prevent an unfair advantage.</p>
            <h2>When</h2>
            <p><strong>11th-13th June 2013</strong></p>
            <p>The event will happen over the course of 3 days, and culminated in a demonstration of outcomes on the Friday. Each team will have one day <strong>starting at 9am</strong>, until 9pm (When security kick you out). It is still a working day and you are expected to complete your usual working hours but beyond that you are free to leave whenever you like.<p>
            <h2>Where</h2>
            <p>Communication is key, In order to reduce disruption to other departments we work alongside. Each team will have their own <strong>meeting room, either LG1, LG2, G1, or G5</strong> for the entire day. To maximise your working time It is recommended for each team to set up their working environment the night before.</p>
            <h2>Teams</h2>
            <p>Everyone Is expected to participate teams of three or four picked at random out of Rob Dean’s magento hat. Teams have been picked to spread expertise and knowledge of different fields evenly. You are expected to work as a team to produces a body of work. it is also acceptable for smaller side projects to be submitted.</p>
            <p>Teams will be announced closer to the time, but in the meantime the day you will be competing is below so you can organise your workload, adjust meetings and other commitments.</p>                         
